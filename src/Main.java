import java.util.Scanner;

public class Main {


    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int m = scanner.nextInt();
        int n = scanner.nextInt();
        for(int i = 1; i <= m; i++){
            System.out.println(8);
        }
        for(int t = 1; t <= n; t++){
            System.out.print(8 + " ");
        }

    }

}
